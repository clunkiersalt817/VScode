#include<iostream>
using namespace std;
int main()
{
    int i;
    float f;
    char c;
    double d;
    long l;
    cout<<"The size of int is "<<sizeof(i)<<endl;
    cout<<"The size of float is "<<sizeof(f)<<endl;
    cout<<"The size of char is "<<sizeof(c)<<endl;
    cout<<"The size of double is "<<sizeof(d)<<endl;
    cout<<"The size of long is "<<sizeof(l)<<endl;
    return 0;
}

